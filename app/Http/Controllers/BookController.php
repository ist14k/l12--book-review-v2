        $book = cache()->remember(
            $cacheKey,
            now()->addMinutes(10),
            fn() => Book::with(['reviews' => fn($query) => $query->latest()])->withAverageRating()->withReviewsCount()->findOrFail($id)
        );

        return view('books.show', compact('book'));
